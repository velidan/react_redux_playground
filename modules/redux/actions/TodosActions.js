
export const addTodo = (text, id) => {
	console.log(text, id);
	return {
		type : "ADD_TODO",
		id : id,
		text
	}
}

export const setVisibilityFilter = (filter) => {
	return {
		type : "SET_VISIBILITY_FILTER",
		filter
	}
}

export const toggleTodo = (id) => {
	return {
		type : "TOGGLE_TODO",
		id
	}
}